#include <stdio.h>
#include <math.h>

int main() {
  printf("sin30°=%f\n", sin(30.0 * 3.14159 / 180.0));
  printf("cos30°=%f\n", cos(30.0 * 3.14159 / 180.0));
  printf("tan45°=%f\n", tan(45.0 * 3.14159 / 180.0));
  printf("2の3乗=%f\n", pow(2.0, 3.0));
  printf("1000の常用対数=%f\n", log10(1000.0));
  printf("10の平方根=%f\n", sqrt(10.0));
  return 0;
}
